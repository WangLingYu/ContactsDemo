public class Person {

    /**
     * person_id : None
     * person_name : None
     * person_address : None
     * person_phone : None
     * person_company_phone : None
     * person_home_phone : None
     */

    private String person_id;
    private String person_name;
    private String person_address;
    private String person_phone;
    private String person_company_phone;
    private String person_home_phone;

    public String getPerson_id() {
        return person_id;
    }

    public void setPerson_id(String person_id) {
        this.person_id = person_id;
    }

    public String getPerson_name() {
        return person_name;
    }

    public void setPerson_name(String person_name) {
        this.person_name = person_name;
    }

    public String getPerson_address() {
        return person_address;
    }

    public void setPerson_address(String person_address) {
        this.person_address = person_address;
    }

    public String getPerson_phone() {
        return person_phone;
    }

    public void setPerson_phone(String person_phone) {
        this.person_phone = person_phone;
    }

    public String getPerson_company_phone() {
        return person_company_phone;
    }

    public void setPerson_company_phone(String person_company_phone) {
        this.person_company_phone = person_company_phone;
    }

    public String getPerson_home_phone() {
        return person_home_phone;
    }

    public void setPerson_home_phone(String person_home_phone) {
        this.person_home_phone = person_home_phone;
    }
}
